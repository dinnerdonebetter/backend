package types

// stringPointer returns a pointer to the provided string.
func stringPointer(x string) *string {
	return &x
}

// uint8Pointer returns a pointer to the provided uint32.
func uint8Pointer(x uint8) *uint8 {
	return &x
}

// uint32Pointer returns a pointer to the provided uint32.
func uint32Pointer(x uint32) *uint32 {
	return &x
}

// boolPointer returns a pointer to the provided string.
func boolPointer(x bool) *bool {
	return &x
}

// float32Pointer returns a pointer to the provided string.
func float32Pointer(x float32) *float32 {
	return &x
}
